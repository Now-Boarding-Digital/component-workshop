export type ColorValue = { hex: string; alias?: string }
export type FloatValue = { value: number; alias?: string }
export type ColorToken = { name: string; type: 'COLOR'; values: Record<string, ColorValue> }
export type FloatToken = { name: string; type: 'FLOAT'; values: Record<string, FloatValue> }
export type StringToken = { name: string; type: 'STRING'; values: Record<string, string> }
export type Token = ColorToken | FloatToken | StringToken
export type Collection = { name: string; modes: string[]; variables: Token[] }

const c = (hex: string): ColorValue => ({ hex })
const f = (value: number, alias?: string): FloatValue => alias ? { value, alias } : { value }
const a = (hex: string, alias: string): ColorValue => ({ hex, alias })

